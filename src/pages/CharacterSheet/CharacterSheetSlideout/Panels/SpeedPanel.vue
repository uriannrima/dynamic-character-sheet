There are three movement scales, as follows.

*   Tactical, for combat, measured in feet (or squares) per round.
*   Local, for exploring an area, measured in feet per minute.
*   Overland, for getting from place to place, measured in miles per hour or miles per day.

##### Modes of Movement

While moving at the different movement scales, creatures generally walk, hustle, or run.

###### Walk
A walk represents unhurried but purposeful movement at 3 miles per hour for an unencumbered human.

###### Hustle
A hustle is a jog at about 6 miles per hour for an unencumbered human. A character moving his or her speed twice in a single round, or moving that speed in the same round that he or she performs a [standard action](/srd/combat/actionsInCombat.htm#standardActions) or another [move action](/srd/combat/actionsInCombat.htm#moveActions) is hustling when he or she moves.

###### Run (×3)
Moving three times speed is a running pace for a character in heavy armor. It represents about 7 miles per hour for a human in full plate.

###### Run (×4)
Moving four times speed is a running pace for a character in light, medium, or no armor. It represents about 14 miles per hour for an unencumbered human, or 10 miles per hour for a human in chainmail.

### Tactical Movement

Use tactical movement for combat. Characters generally don’t walk during combat—they hustle or run. A character who moves his or her speed and takes some action is hustling for about half the round and doing something else the other half.

##### Hampered Movement
Difficult terrain, obstacles, or poor visibility can hamper movement. When movement is hampered, each square moved into usually counts as two squares, effectively reducing the distance that a character can cover in a move.

If more than one condition applies, multiply together all additional costs that apply. (This is a specific exception to the normal rule for doubling)

In some situations, your movement may be so hampered that you don’t have sufficient speed even to move 5 feet (1 square). In such a case, you may use a [full-round action](/srd/combat/actionsInCombat.htm#fullRoundActions) to move 5 feet (1 square) in any direction, even diagonally. Even though this looks like a 5-foot step, it’s not, and thus it provokes [attacks of opportunity](/srd/combat/attacksOfOpportunity.htm) normally. (You can’t take advantage of this rule to move through impassable terrain or to move when all movement is prohibited to you.)

You can’t run or [charge](/srd/combat/specialAttacks.htm#charge) through any square that would hamper your movement.

### Local Movement

Characters exploring an area use local movement, measured in feet per minute.

##### Walk
A character can walk without a problem on the local scale.

##### Hustle
A character can hustle without a problem on the local scale. See Overland Movement, below, for movement measured in miles per hour.

##### Run
A character with a Constitution score of 9 or higher can run for a minute without a problem. Generally, a character can run for a minute or two before having to rest for a minute.

### Overland Movement

Characters covering long distances cross-country use overland movement. Overland movement is measured in miles per hour or miles per day. A day represents 8 hours of actual travel time. For rowed watercraft, a day represents 10 hours of rowing. For a sailing ship, it represents 24 hours.

##### Walk
A character can walk 8 hours in a day of travel without a problem. Walking for longer than that can wear him or her out (see [Forced March](#forcedMarch), below).

##### Hustle
A character can hustle for 1 hour without a problem. Hustling for a second hour in between sleep cycles deals 1 point of [nonlethal damage](/srd/combat/injuryandDeath.htm#nonlethalDamage), and each additional hour deals twice the damage taken during the previous hour of hustling. A character who takes any nonlethal damage from hustling becomes [fatigued](/srd/conditionSummary.htm#fatigued).

A fatigued character can’t run or [charge](/srd/combat/specialAttacks.htm#charge) and takes a penalty of -2 to Strength and Dexterity. Eliminating the [nonlethal damage](/srd/combat/injuryandDeath.htm#nonlethalDamage) also eliminates the fatigue.

##### Run
A character can’t run for an extended period of time.

Attempts to run and rest in cycles effectively work out to a hustle.

##### Terrain
The terrain through which a character travels affects how much distance he or she can cover in an hour or a day (see [Table: Terrain and Overland Movement](#tableTerrainAndOverlandMovement)). A highway is a straight, major, paved road. A road is typically a dirt track. A trail is like a road, except that it allows only single-file travel and does not benefit a party traveling with vehicles. Trackless terrain is a wild area with no paths.

##### Forced March
In a day of normal walking, a character walks for 8 hours. The rest of the daylight time is spent making and breaking camp, resting, and eating.

A character can walk for more than 8 hours in a day by making a forced march. For each hour of marching beyond 8 hours, a Constitution check (DC 10, +2 per extra hour) is required. If the check fails, the character takes 1d6 points of [nonlethal damage](/srd/combat/injuryandDeath.htm#nonlethalDamage). A character who takes any nonlethal damage from a forced march becomes [fatigued](/srd/conditionSummary.htm#fatigued). Eliminating the nonlethal damage also eliminates the fatigue. It’s possible for a character to march into unconsciousness by pushing himself too hard.

##### Mounted Movement
A mount bearing a rider can move at a hustle. The damage it takes when doing so, however, is lethal damage, not [nonlethal damage](/srd/combat/injuryandDeath.htm#nonlethalDamage). The creature can also be ridden in a [forced march](#forcedMarch), but its Constitution checks automatically fail, and, again, the damage it takes is lethal damage. Mounts also become [fatigued](/srd/conditionSummary.htm#fatigued) when they take any damage from hustling or forced marches.

See [Table: Mounts and Vehicles](#tableMountsAndVehicles) for mounted speeds and speeds for vehicles pulled by draft animals.

##### Waterborne Movement
See [Table: Mounts and Vehicles](#tableMountsAndVehicles) for speeds for water vehicles.

### Moving In Three Dimensions

#### Tactical Aerial Movement
Once movement becomes three-dimensional and involves turning in midair and maintaining a minimum velocity to stay aloft, it gets more complicated. Most [flying creatures](/srd/specialAbilities.htm#fly) have to slow down at least a little to make a turn, and many are limited to fairly wide turns and must maintain a minimum forward speed. Each flying creature has a maneuverability, as shown on [Table: Maneuverability](#tableManeuverability). The entries on the table are defined below.

###### Minimum Forward Speed
If a flying creature fails to maintain its minimum forward speed, it must land at the end of its movement. If it is too high above the ground to land, it falls straight down, descending 150 feet in the first round of falling. If this distance brings it to the ground, it takes falling damage. If the fall doesn’t bring the creature to the ground, it must spend its next turn recovering from the stall. It must succeed on a DC 20 [Reflex save](/srd/combat/combatStatistics.htm#reflex) to recover. Otherwise it falls another 300 feet. If it hits the ground, it takes falling damage. Otherwise, it has another chance to recover on its next turn.

###### Hover
The ability to stay in one place while airborne.

###### Move Backward
The ability to move backward without turning around.

###### Reverse
A creature with good maneuverability uses up 5 feet of its speed to start flying backward.

###### Turn
How much the creature can turn after covering the stated distance.

###### Turn in Place
A creature with good or average maneuverability can use some of its speed to turn in place.

###### Maximum Turn
How much the creature can turn in any one space.

###### Up Angle
The angle at which the creature can climb.

###### Up Speed
How fast the creature can climb.

###### Down Angle
The angle at which the creature can descend.

###### Down Speed
A flying creature can fly down at twice its normal flying speed.

###### Between Down and Up
An average, poor, or clumsy flier must fly level for a minimum distance after descending and before climbing. Any flier can begin descending after a climb without an intervening distance of level flight.

### Evasion And Pursuit

In round-by-round movement, simply counting off squares, it’s impossible for a slow character to get away from a determined fast character without mitigating circumstances. Likewise, it’s no problem for a fast character to get away from a slower one.

When the speeds of the two concerned characters are equal, there’s a simple way to resolve a chase: If one creature is pursuing another, both are moving at the same speed, and the chase continues for at least a few rounds, have them make opposed Dexterity checks to see who is the faster over those rounds. If the creature being chased wins, it escapes. If the pursuer wins, it catches the fleeing creature.

Sometimes a chase occurs overland and could last all day, with the two sides only occasionally getting glimpses of each other at a distance. In the case of a long chase, an opposed Constitution check made by all parties determines which can keep pace the longest. If the creature being chased rolls the highest, it gets away. If not, the chaser runs down its prey, outlasting it with stamina.

### Moving Around In Squares

In general, when the characters aren’t engaged in round-by-round combat, they should be able to move anywhere and in any manner that you can imagine real people could. A 5-foot square, for instance, can hold several characters; they just can’t all fight effectively in that small space. The rules for movement are important for combat, but outside combat they can impose unnecessary hindrances on character activities.`;
